package com.petkov.firstkotlinprogram

fun main(args: Array<String>) {
    var a:Boolean = true
    var b:Char = 'R'
    var c:Byte = 10
    var d:Short = 100
    var e:Int = 1000
    var f:Long = 10000
    var g:Float = 5.33F
    var h:Double = 7.333
    // print the values in one line
    println("Boolean: $a, Char: $b, Byte: $c, Short: $d, Int: $e, Long: $f, Float: $g, Double: $h")

}