package com.petkov.firstkotlinprogram

fun main(args: Array<String>) {

    var a:Double = 7.6
    var b:Boolean = true
    println("+a = " + +a)
    println("-a = " + -a)
    println("++a = " + ++a)
    println("--a = " + --a)
    println("!b = " + !b)

    println("-------------- " )
    var c:Double = 4.2
    println("++c = " + c++)
    println("c = " + c)
    println("--c = " + --c)
    println("c = " + c)
}